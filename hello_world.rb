# hello_world.rb

# String
greeting = "Hello, World!"
puts greeting

# Integer
age = 30
puts "I am #{age} years old."

# Float
height = 5.9
puts "I am #{height} feet tall."

# Boolean
is_happy = true
puts "Am I happy? #{is_happy}"

# Array
fruits = ["apple", "banana", "cherry"]
puts "I like #{fruits.join(', ')}."
