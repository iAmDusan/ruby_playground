# utils/variables.rb

def display_variables
    puts "Demonstrating variables and data types in Ruby:"
    
    # String
    greeting = "Hello, World!"
    puts "String: #{greeting}"
    
    # Integer
    age = 36
    puts "Integer: I am #{age} years old."
    
    # Float
    height = 6.0
    puts "Float: I am #{height} feet tall."
    
    # Boolean
    is_happy = true
    puts "Boolean: Am I happy? #{is_happy}"
    
    # Array
    fruits = ["apple", "banana", "cherry"]
    puts "Array: I like #{fruits.join(', ')}."
  end
  